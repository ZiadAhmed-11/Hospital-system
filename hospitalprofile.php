<br><h6>History and Tradition</h6>
<p>The Hospital was founded in 1780 by Lady Hartstonge and other 
    benefactors as a Fever and Lock Hospital. It treated epidemics 
    during the Great Famine (1845-1847). The Hospital fell into disrepair during 
    the 1850’s to the 1880’s. In 1888 the then Bishop of Limerick,
     Bishop Edward Thomas O’Dwyer invited the Nursing Sisters of the
      Little Company of Mary to the Hospital. St. John’s was the first site in Ireland where the Little Company of Mary Nursing Sisters came to work. Since then, on-going refurbishment and development has continued to take place. With the closure of Barrington’s Hospital in 1988, St. John’s expanded its role, particularly in relation to Accident & Emergency. There has been continuous development since then.

